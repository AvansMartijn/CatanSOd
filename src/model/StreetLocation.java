package model;

public class StreetLocation {

	private int xStart;
	private int yStart;
	
	private int xEnd;
	private int yEnd;
	
	public StreetLocation(int xStart, int yStart, int xEnd, int yEnd) {
		
		this.xStart = xStart;
		this.yStart = yStart;
		
		this.xEnd = xEnd;
		this.yEnd = yEnd;
		
	}
	
}
